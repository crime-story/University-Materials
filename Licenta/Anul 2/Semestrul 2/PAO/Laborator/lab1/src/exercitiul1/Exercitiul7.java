package exercitiul1;

public class Exercitiul7 {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++) {
            System.out.println(i);
        }

        System.out.println();


        for (int i = 5; i >= 0; i--) {
            System.out.println(i);
        }

        System.out.println();

        for (int i = 5; i >= 0; i-=2) {
            System.out.println(i);
        }

    }
}
